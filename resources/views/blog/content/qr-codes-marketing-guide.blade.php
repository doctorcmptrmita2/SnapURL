<h2 class="text-2xl font-bold text-slate-900 dark:text-white mt-10 mb-4">What Are QR Codes?</h2>

<p class="text-slate-600 dark:text-slate-300 mb-6">
    QR codes, or Quick Response codes, are two-dimensional barcodes that can store various types of data, including URLs, text, and contact information. Unlike traditional barcodes that can only be read horizontally, QR codes can be scanned from any direction, making them incredibly versatile for <strong>marketing campaigns</strong>.
</p>

<p class="text-slate-600 dark:text-slate-300 mb-6">
    First developed in 1994 by a Japanese company for tracking automotive parts, QR codes have evolved into powerful marketing tools. Today, virtually every smartphone can scan these codes instantly, making them accessible to billions of potential customers worldwide.
</p>

