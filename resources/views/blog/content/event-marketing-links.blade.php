<h2 class="text-2xl font-bold text-slate-900 dark:text-white mt-10 mb-4">Email Campaign Tracking</h2>

<p class="text-slate-600 dark:text-slate-300 mb-6">
    Email typically drives the majority of <strong>event registrations</strong>. Create unique shortened URLs for each email send to compare performance. Track which subject lines, send times, and content drive the most clicks.
</p>

<p class="text-slate-600 dark:text-slate-300 mb-6">
