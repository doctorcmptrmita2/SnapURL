<h2 class="text-2xl font-bold text-slate-900 dark:text-white mt-10 mb-4">How URL Shortening Technology Works</h2>

<p class="text-slate-600 dark:text-slate-300 mb-6">
    The technology behind URL shorteners is elegantly simple yet highly effective. When you submit a long URL to a shortening service, the system generates a unique identifier—typically a combination of letters and numbers. This identifier is then appended to the shortener's domain, creating your new short link.
</p>

<p class="text-slate-600 dark:text-slate-300 mb-6">
    For example, a URL like <code class="bg-slate-100 dark:bg-slate-800 px-2 py-1 rounded">https://example.com/products/category/subcategory/item-name-with-long-description</code> might become <code class="bg-slate-100 dark:bg-slate-800 px-2 py-1 rounded">https://snapurl.to/abc123</code>. The shortener maintains a database that maps each short code to its corresponding original URL.
</p>

