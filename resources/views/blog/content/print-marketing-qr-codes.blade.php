<h2 class="text-2xl font-bold text-slate-900 dark:text-white mt-10 mb-4">Design Best Practices</h2>

<p class="text-slate-600 dark:text-slate-300 mb-6">
    QR codes don't have to be ugly black-and-white squares. Customize colors to match your brand while maintaining sufficient contrast for scanning. Add logos to the center for brand recognition without compromising functionality.
</p>

<p class="text-slate-600 dark:text-slate-300 mb-6">
    Always test printed QR codes before mass production. Print quality, paper finish, and lighting conditions all affect scannability. A code that works on screen may fail in print.
</p>

