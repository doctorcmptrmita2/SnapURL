<h2 class="text-2xl font-bold text-slate-900 dark:text-white mt-10 mb-4">How Link Retargeting Works</h2>

<p class="text-slate-600 dark:text-slate-300 mb-6">
    When you create a retargeting-enabled short link, you attach pixel codes from advertising platforms like Facebook, Google, or LinkedIn. When someone clicks the link, the redirect page briefly loads these pixels before forwarding to the destination.
</p>

<p class="text-slate-600 dark:text-slate-300 mb-6">
    The process happens in milliseconds—users barely notice the redirect. But in that brief moment, their browser receives the retargeting cookie, adding them to your custom audience for future advertising.
</p>

